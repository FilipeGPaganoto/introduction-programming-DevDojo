package academy.devdojo.variables;

public class Variables02 {
    public static void main(String[] args) {
        byte age = 17;
        short houseNumber = 1000;
        int counter = 2000000000;
        long serialNumber = 239249320;
        float regularSalary = 5000.5F;
        double elonMuskFortune = 12200.0;
        boolean condition = false;
        char gender = 'M';
        System.out.println("Your age is: " + age);
        System.out.println("Your house number is: " + houseNumber);
        System.out.println("Your counter is: " + counter);
        System.out.println("Your serial number is: " + serialNumber);
        System.out.println("Your regular salary is: " + regularSalary);
        System.out.println("Your Elon Musk Fortune is: " + elonMuskFortune);
        System.out.println("Your condition is: " + condition);
        System.out.println("Your gender is: " + gender);
    }
}
